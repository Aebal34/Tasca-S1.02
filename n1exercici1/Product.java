package n1exercici1;

public class Product {

	//ATTRIBUTES
	
	private String name;
	private int price;
	
	//CONSTRUCTOR
	
	public Product(String name, int price) {
		this.name = name;
		this.price = price;
	}
	//GETTERS
	
	public String getName() {
		return name;
	}
	public int getPrice() {
		return price;
	}
}
